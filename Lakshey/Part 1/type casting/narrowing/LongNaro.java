class LongNaro
{
  public static void main(String [] args)
  {
    long l =98;
    int i = (int) l;
    short s = (short) l;
    byte b = (byte) l;
    char c = (char) l;
    System.out.println(b);
    System.out.println(s);
    System.out.println(i);
    System.out.println(l);
    System.out.print(c);
}
}